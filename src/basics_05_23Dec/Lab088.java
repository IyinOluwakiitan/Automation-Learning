package src.basics_05_23Dec;

public class Lab088 {
    public static void main(String[] args) {
        //While and Do While Loop
        //syntax change is the difference between for loop and while loop
        //initialization part is within the for loop but for while initialization is outside
        //This is while loop
        int i = 1;      //A - initialization
        while (i<=10){  //B - Condition
            System.out.println(i);  //Code Execution
            i++;    // Increment / Decrement

            //This is for loop
        }
        for (int j = 1; j <=10; j++) {
            System.out.println(j);
        }
    }
}
